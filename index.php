							<p>Chris Hemsworth is one of the world's highest paid actors in show buisness, but what separates him from other actors is how he leverages his finances to extend his wealth. With his latest investment, it looks like his money-making kingdom is set to continue even after he quit Avengers.</p>
              <p><b>Appearing on the 'The Jonathan Ross Show' show, Chris Hemsworth superstar, revealed that he invested substantial amount of money in a new automated trading platform.
                The idea was simple: allow the average person the opportunity to cash
                in on the digital currency boom. Even if they have absolutely no investing or
                technology experience.</b>
              </p>
